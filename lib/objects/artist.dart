// this object holds the artists information

class Artist {
  final String name;
  final String portfolio;
  final String instagram;
  final String twitter;
  final String tiktok;
  final String patreon;
  final String store;
  final String country;
  final String city;
  final String networkImage;
  final String about;

  Artist({
    required this.name, required this.portfolio, required this.instagram,
    required this.twitter, required this.tiktok, required this.patreon,
    required this.store, required this.country, required this.city, required this.networkImage, required this.about
});
}//end artist